package and.gesture.eiger;

public class Point 
{
	
	private float X;
	private float Y;
	
	public Point(){}
	public Point(float X,float Y)
	{
		this.X = X;
		this.Y = Y;
	}
	
	public float getX()
	{
		return X;
	}
	
	public float getY()
	{
		return Y;
	}
	public void setX(float x) {
		X = x;
	}
	public void setY(float y) {
		Y = y;
	}
	
	
}
